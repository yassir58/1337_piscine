int	ft_sqrt(int nb)
{
	int	i;
	int	res ;

	i = 0;
	res = 0 ;
	while (i <= 46340)
	{
		if (i * i == nb)
		{
			res = i;
			break ;
		}
		i++;
	}
	return (res);
}
